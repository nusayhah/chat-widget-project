const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const morgan = require('morgan');
const rateLimit = require('express-rate-limit');
require('dotenv').config();

const WebSocket = require('ws');
const ChatHandler = require('./websocket/chatHandler');

const { testConnection } = require('./config/database');
const authRoutes = require('./routes/auth');
const widgetRoutes = require('./routes/widgets');

const app = express();
const PORT = process.env.PORT || 5000;

// Security middleware
app.use(helmet());

// CORS configuration
const corsOptions = {
  origin: process.env.ALLOWED_ORIGINS?.split(',') || ['http://localhost:3001', 'http://localhost:3000'],
  credentials: true,
  optionsSuccessStatus: 200
};
app.use(cors(corsOptions));

// Rate limiting
const limiter = rateLimit({
  windowMs: parseInt(process.env.RATE_LIMIT_WINDOW_MS) || 15 * 60 * 1000, // 15 minutes
  max: parseInt(process.env.RATE_LIMIT_MAX_REQUESTS) || 100, // limit each IP to 100 requests per windowMs
  message: {
    success: false,
    message: 'Too many requests from this IP, please try again later.'
  }
});
app.use('/api/', limiter);

// Logging middleware
app.use(morgan('combined'));

// Body parsing middleware
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true, limit: '10mb' }));

// Health check endpoint
app.get('/health', (req, res) => {
  res.json({
    success: true,
    message: 'Chat Widget API is running',
    timestamp: new Date().toISOString(),
    environment: process.env.NODE_ENV || 'development'
  });
});

// API routes
app.use('/api/auth', authRoutes);
app.use('/api/widgets', widgetRoutes);

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({
    success: false,
    message: 'Endpoint not found'
  });
});

// Global error handler
app.use((error, req, res, next) => {
  console.error('Global error handler:', error);
  
  res.status(error.status || 500).json({
    success: false,
    message: process.env.NODE_ENV === 'production' 
      ? 'Internal server error' 
      : error.message,
    ...(process.env.NODE_ENV !== 'production' && { stack: error.stack })
  });
});

// Start server
const startServer = async () => {
  try {
    // Test database connection
    const dbConnected = await testConnection();
    if (!dbConnected) {
      console.error('âŒ Failed to connect to database. Server not started.');
      process.exit(1);
    }

    const server = app.listen(PORT, () => {
      console.log(`ğŸš€ Chat Widget API server running on port ${PORT}`);
      console.log(`ğŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
      console.log(`ğŸ”— Health check: http://localhost:${PORT}/health`);
    });

    // ğŸ†• ADD WEB SOCKET SERVER
    const wss = new WebSocket.Server({ server });
    new ChatHandler(wss);
    console.log(`ğŸ”Œ WebSocket server running on port ${PORT}`);
    
  } catch (error) {
    console.error('âŒ Failed to start server:', error);
    process.exit(1);
  }
};

startServer();

module.exports = app;